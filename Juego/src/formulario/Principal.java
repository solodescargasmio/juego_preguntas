    public void LlenarHistorico() throws SQLException, ClassNotFoundException{
        tblRanking.setModel(new DefaultTableModel());
        String[] columnas={"Jugador","Nivel","Premio","Tipo"};
        String[] registros=new String[4];
        modelo=new DefaultTableModel(null,columnas);
        modelo.setColumnIdentifiers(columnas);
        tblRanking.setModel(modelo);
        Conectar cnn=new Conectar();
        ArrayList<Estadistica> estadisticas=new ArrayList<Estadistica>();
        ControladorEstadistica conEsta=new ControladorEstadistica();
        estadisticas=conEsta.getAllEstadistica(cnn.obtener());
        if(estadisticas.size()>0){
            for(Estadistica estadistica:estadisticas){
              registros[0]=estadistica.getJugador();
              registros[1]=Integer.toString(estadistica.getIdRonda());
              registros[2]=Integer.toString(estadistica.getPremio());
              if(estadistica.getTipo_P()==0){
                  registros[3]="Puntos";
              }else{
                registros[3]="Dinero";
              }
              
              modelo.addRow(registros);
            }
            tblRanking.setModel(modelo);
        }
        
    }
    
