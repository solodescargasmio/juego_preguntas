    private void btnResponderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResponderActionPerformed
        // TODO add your handling code here:
        ControladorOpcion cOp=new ControladorOpcion();
        Opcion opcion=new Opcion();
        int nOp=0;

        if(jrb1.isSelected()){
            nOp=Integer.parseInt(lbrb1.getText());
        }else if(jrb2.isSelected()){
            nOp=Integer.parseInt(lbrb2.getText());
        }else if(jrb3.isSelected()){
            nOp=Integer.parseInt(lbrb3.getText());
        }else if(jrb4.isSelected()){
            nOp=Integer.parseInt(lbrb4.getText());
        }else{
            JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR ALGUNA OPCION");
        }
        if(nOp>0){
            Conectar cnn=new Conectar();
            try {
                opcion=cOp.getOpcion(cnn.obtener(), nOp);
                if(opcion.isbCorrecta()){
                    JOptionPane.showMessageDialog(null, "CORRECTO");
                    
                    int nCat=Integer.parseInt(lbidCat.getText());
                    ObtenerPremio(nCat);
                    if(nCat==5){
                        JOptionPane.showMessageDialog(null, "FELICITACIONES, RETO SUPERADO. PUEDES VOLVER A INTENTARLO");         
                        lbRonda.setText("6");
                        GrardarEstadistica();
                        InicializarJuego();
                    }
                    if(nCat<5){
                    LimpiarRadioButton();
                    nCat=nCat+1;
                    
                    switch(nCat){
                        case 2:
                                prb2.setValue(100);
                            break;
                        case 3:
                               prb3.setValue(100);
                            break;
                        case 4:
                                prb4.setValue(100);
                            break;
                        case 5:
                                prb5.setValue(100);         
                            break;   
                    }
                    lbidCat.setText(Integer.toString(nCat));
                    lbRonda.setText(Integer.toString(nCat));
                    HabMant();
                    }
                       
                }else{
                    JOptionPane.showMessageDialog(null, "RESPUESTA EQUIVOCADA. GRACIAS POR PARTICIPAR.");
                     GrardarEstadistica();
                     InicializarJuego();

                }
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnResponderActionPerformed
