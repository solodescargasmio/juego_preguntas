    public int DevolverIdPregunta(int nIndex){
        int nId=0;
        Conectar cnn=new Conectar();
        ControladorPregunta conPre=new ControladorPregunta();
        Pregunta pregunta=new Pregunta();
        ArrayList<Pregunta> preguntas = new ArrayList<Pregunta>();
        try {
            preguntas=conPre.getPreguntasCategorias(cnn.obtener(), Integer.parseInt(lbidCat.getText()));
            pregunta=preguntas.get(nIndex-1);
            nId=pregunta.getId();
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        return nId;
    }
    public void PreguntaAleatoria(){
        if(ValidarNick()){
            HabMant();
           HabOpc(true);
            Random r=new Random();
             int nAzar=r.nextInt(5)+1;
             if(nAzar==0){
             nAzar=1;
             }
             Conectar cnn=new Conectar();
             lbNumRandom.setText(Integer.toString(nAzar));
             ControladorPregunta conPre=new ControladorPregunta();
             Pregunta pregunta=new Pregunta();
             int nCat=0;
             if(lbidCat.getText()!=""){
                 nCat=Integer.parseInt(lbidCat.getText());
             }
             nAzar=DevolverIdPregunta(nAzar);
             try {
                 pregunta=conPre.getPregunta(cnn.obtener(), nAzar, nCat);
                 txaPregunta.setText(pregunta.getsPregunta());
                 TraerOpciones(nAzar);
             } catch (SQLException ex) {
                 Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
             }
             prb1.setValue(100);
       }else{
       JOptionPane.showMessageDialog(null, "INGRESE UN NICK PARA JUGAR");
       }
    }
    
